DROP TABLE articles CASCADE CONSTRAINTS;
DROP TABLE users CASCADE CONSTRAINTS;
DROP TABLE comments CASCADE CONSTRAINTS;
DROP TABLE categories CASCADE CONSTRAINTS;
DROP TABLE tags CASCADE CONSTRAINTS;
DROP TABLE art_cat CASCADE CONSTRAINTS;
DROP TABLE art_tag CASCADE CONSTRAINTS;

CREATE TABLE articles
(
    article_id INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    tname VARCHAR2(255) NOT NULL,
    creation_date DATE NOT NULL,
    user_id INTEGER,
    text VARCHAR2(2500) NOT NULL
);

CREATE TABLE users
(
    u_id INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    u_name VARCHAR2(255 CHAR) NOT NULL,
    u_mail VARCHAR2(255 CHAR) NOT NULL
);

CREATE TABLE comments
(
    com_id INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    commentary VARCHAR2(500 CHAR) NOT NULL,
    u_id INTEGER NOT NULL,
    url INTEGER NOT NULL
);

CREATE TABLE categories
(
    cat_id INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    cat_name VARCHAR2(255 CHAR) NOT NULL
);

CREATE TABLE tags
(
    tag_id INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    tag_name VARCHAR2(255 CHAR) NOT NULL
);

CREATE TABLE art_cat
(
    id_art INTEGER NOT NULL,
    id_cat INTEGER NOT NULL
);

CREATE TABLE art_tag
(
    id_art INTEGER NOT NULL,
    id_tag INTEGER NOT NULL
);